var exec = require('child_process').exec;
var fs = require('fs-extra');

function shell(string) { console.log(string); exec(string, puts); }
function puts(error, stdout, stderr) {
    if (error === null) { console.log(stdout); } else { console.log('exec error: ' + error); }
}

var environment = process.env.NODE_ENV;
// prossible values: darwin (OSX), freebsd, linux, sunos, win32
var platform = process.platform;
var version = process.version;

//////////////////////////////////////////////////////////////////////////////////////
///// the fun starts here ////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////

module.exports = {
    run: shell,
    env: environment,
    version: version,
    platform: platform,
    fs: fs
};